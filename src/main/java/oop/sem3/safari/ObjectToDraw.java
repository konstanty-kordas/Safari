/**
 *
 * @author konstanty
 */
public class ObjectToDraw {
    private int coordX;
    private int coordY;
    
    
    /**
     * Draws the object
     */
    public void draw(){}

    /**
     * @return the coordX
     */
    public int getCoordX() {
        return coordX;
    }

    /**
     * @param coordX the coordX to set
     */
    public void setCoordX(int coordX) {
        this.coordX = coordX;
    }

    /**
     * @return the coordY
     */
    public int getCoordY() {
        return coordY;
    }

    /**
     * @param coordY the coordY to set
     */
    public void setCoordY(int coordY) {
        this.coordY = coordY;
    }
}
