package oop.sem3.safari;

/**
 *
 * @author konstanty
 */
public class Animal extends ObjectToDraw {
    private String name;
    private int health;
    private int speed;
    private int energy;
    private int water;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the health
     */
    public int getHealth() {
        return health;
    }

    /**
     * @param health the health to set
     */
    public void setHealth(int health) {
        this.health = health;
    }

    /**
     * @return the speed
     */
    public int getSpeed() {
        return speed;
    }

    /**
     * @param speed the speed to set
     */
    public void setSpeed(int speed) {
        this.speed = speed;
    }

    /**
     * @return the energy
     */
    public int getEnergy() {
        return energy;
    }

    /**
     * @param energy the energy to set
     */
    public void setEnergy(int energy) {
        this.energy = energy;
    }

    /**
     * @return the water
     */
    public int getWater() {
        return water;
    }

    /**
     * @param water the water to set
     */
    public void setWater(int water) {
        this.water = water;
    }
    
    
    
}
